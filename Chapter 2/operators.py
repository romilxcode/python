#Arithmetic operators
x= 5
y= 10

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y)
print(x**y)

#Comparison operators

print(x==y)
print(x>=y)
print(x<=y)
print(x>y)
print(x<y)

#logical operators

print("And Operator Result:", x>y & x<y)
print("Or Operator Result:", x<y / x>y)
print("Not Operator Result:", not(x>y))