#Create a set of numbers and show union and intersection operations with another set.

setA = {1, 2, 3, 4, 5}
setB = {4, 5, 6, 7, 8}
print("Set A:", setA)
print("Set B:", setB)

# Union
unionSet = setA.union(setB)
print("Union of Set A and Set B:", unionSet)

# Intersection
intersectionSet = setA.intersection(setB)
print("Intersection of Set A and Set B:", intersectionSet)