# Explain the difference between local and global scope with your own words.

# In programming, scope refers to the visibility and accessibility of variables in different parts of the code.
# A global variable is defined outside of any function and can be accessed from anywhere in the code, including inside functions.
# A local variable, on the other hand, is defined within a function and can only be
# accessed from within that function. Local variables are created when the function is called and destroyed when the function exits.